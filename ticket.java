    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         String tid = txttid.getText();
         String fid = txtflightno.getText();
         String id = txtcustid.getText();
         String flightclass = txtclass.getSelectedItem().toString();
         String seat = txtseat.getValue().toString();
         String totalprice = txttotal.getText();
         String date =txtdate.getText();
       
         if(!"-".equals(fid)&&!"0".equals(seat)&&!"".equals(id)&&!"-".equals(date)&&!"-".equals(totalprice)){
             
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pt=con.prepareStatement("insert into ticket(tid,fid,id,class,seats,price,date)values(?,?,?,?,?,?,?)");
            pt.setString(1,tid);
            pt.setString(2,fid);
            pt.setString(3,id);
            pt.setString(4,flightclass);
            pt.setString(5,seat);
            pt.setString(6,totalprice);
            pt.setString(7,date);
                     
            pt.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Ticket Booked");
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ticket.class.getName()).log(Level.SEVERE, null, ex);
        }
       }
         else{
             JOptionPane.showMessageDialog(null, "Invalid! Please Fill Everything");
         }
    }//GEN-LAST:event_jButton2ActionPerformed

