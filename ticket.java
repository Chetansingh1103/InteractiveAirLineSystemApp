    private void txtidsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtidsearchActionPerformed
        // TODO add your handling code here:
        String id = txtcustid.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pt=con.prepareStatement("select * from customer where id = ?");
            pt.setString(1,id);
            ResultSet rs = pt.executeQuery();
            
            if(rs.next()==false){
                JOptionPane.showMessageDialog(this,"Record Not Present");
            }
            else{
                String firstname=rs.getString("firstname");
                String lastname=rs.getString("lastname");             
                String passportno=rs.getString("passport");               
                txtfirstname.setText(firstname);
                txtlastname.setText(lastname);
                txtpassportNo.setText(passportno);       
            }
                               
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Search_Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtidsearchActionPerformed

